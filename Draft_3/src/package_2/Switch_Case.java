package package_2;

public class Switch_Case {

	public static void main(String[] args) {
		int number = 1000;

		switch (number) {
		case 100: {
			System.out.println("this is 100");
			break;
		}
		case 1000: {
			System.out.println("this is 1000");
			break;
		}
		default:
			System.out.println("another number");
		}

	}

}
